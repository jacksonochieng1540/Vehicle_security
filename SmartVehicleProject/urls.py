from django.contrib import admin
from django.urls import path, include
from django.conf import settings
from django.conf.urls.static import static
from django.views.generic import RedirectView

urlpatterns = [
    path('admin/', admin.site.urls),
    path('', RedirectView.as_view(url='/dashboard/', permanent=False), name='home'),
    path('auth/', include('authentication.urls')),
    path('dashboard/', include(('vehicle_tracking.urls', 'dashboard'), namespace='dashboard')),
    path('alerts/', include(('alerts.urls', 'alerts'), namespace='alerts')),
    path('hardware/', include(('hardware.urls', 'hardware'), namespace='hardware')),
]

# Serve media files in development
if settings.DEBUG:
    urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
    urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)

# Admin site customization
admin.site.site_header = "Vehicle Security System"
admin.site.site_title = "Vehicle Security Admin"
admin.site.index_title = "Welcome to Vehicle Security System Administration"